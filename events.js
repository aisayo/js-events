// JavaScript in the browser uses an event-driven programming model.
// Events are actions that happen when a user interacts with the page
// Clicking an element, typing in a field, or loading a page.
// The browser detects a change, and alerts a function (event handler) that is listening to a particular event. 
// These functions then perform the actions as desired.

// Some common events: 
// onclick 
// dblclick 
// mousedown 
// mouseup 
// mousemove 
// keydown 
// keyup 
// touchmove 
// touchstart 
// touchend 
// onload 
// onfocus 
// onblur 
// onerror 
// onscroll

// Resources:
// https://javascript.info/introduction-browser-events
// https://developer.mozilla.org/en-US/docs/Web/Events
